// And Or Operators
let marriedTo =  "Parteek";
let wife = "Kamini";

// And Operator
if(marriedTo === "Parteek" && wife === "Kamini"){
    console.log('yay ❤');
}else{
    console.log('oh shit 🤷‍♀️');
}

// Or Operator
if(marriedTo === "arteek" || wife === "amini"){
    console.log('yay ❤');
}else{
    console.log('oh shit 🤷‍♀️');
}

// Check the first character of the name
if(marriedTo[0] === "P" || wife[0] === "K"){
    console.log('yay PK ❤');
}else{
    console.log('oh shit PK 🤷‍♀️');
}

