// Conventions of Naming Variables:

// 1Value //Invalid
// Value1 //Valid

// Only use underscore or dollar
// $firstName  //Valid
// first$name  //Valid

// _firstName  //Valid
// first_Name  //valid

// first Name // No spaces //Invalid
// No use of * & @

// Casing
// first_name // snake case writing
// firstName // camel case writing  preffered

var $amount = 2;
var $var1 = 200;
var $var2 = 400;

console.log($amount * 0.5); // multiplication

// ** means raise to the power
console.log($amount ** 0.5); //sq root   
console.log($amount ** ($var1/$var2)); //sq root

console.log($amount ** 2); // square
console.log($amount ** 3); // cube
